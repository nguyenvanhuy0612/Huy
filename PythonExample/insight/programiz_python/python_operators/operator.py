# Comparison operators in Python
x = 10
y = 12

# Output: x > y is False
print('x > y is', x > y)

# Output: x < y is True
print('x < y is', x < y)

# Output: x == y is False
print('x == y is', x == y)

# Output: x != y is True
print('x != y is', x != y)

# Output: x >= y is False
print('x >= y is', x >= y)

# Output: x <= y is True
print('x <= y is', x <= y)

# Logical operators
x = True
y = False

print('x and y is', x and y)

print('x or y is', x or y)

print('not x is', not x)

# Bitwise operators
